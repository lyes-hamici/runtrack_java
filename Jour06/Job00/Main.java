public class Main {
   
    public static void main(String[] args) {
        // Déclaration des variables
        char lettre = 'A';
        String texte = "Bonjour";
        int nombreEntier = 100;
        long nombreLong = 123456789L;
        float nombreFlottant = 10.5f;
        boolean estVrai = true;

        // Affichage des valeurs stockées
        System.out.println("Valeur du char : " + lettre);
        System.out.println("Valeur du string : " + texte);
        System.out.println("Valeur de l'int : " + nombreEntier);
        System.out.println("Valeur du long : " + nombreLong);
        System.out.println("Valeur du float : " + nombreFlottant);
        System.out.println("Valeur du boolean : " + estVrai);

        // Stocker 3.817 dans une variable int
        int TOTO = (int) 3.817;
        System.out.println("Valeur de TOTO après stockage de 3.817 : " + TOTO);
    }
    
    
}